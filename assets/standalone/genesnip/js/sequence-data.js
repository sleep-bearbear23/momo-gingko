// Utility Functions
/**
 * Generates a random number between min and max.
 * @param {number} min - Minimum value.
 * @param {number} max - Maximum value.
 * @returns {number} Random number.
 */
function random(min, max) {
    return min + Math.random() * (max - min);
}

/**
 * Computes overall sequence metrics.
 * @param {Array} data - DNA data array.
 * @returns {Object} Metrics including hamming distance, error rate, percent identity, and error margin.
 */
function computeOverallMetrics(data) {
    const hamming = data.reduce((acc, item) => acc + (item.pn !== item.tn ? 1 : 0), 0);
    const errorRate = ((hamming / data.length) * 100).toFixed(2);
    const percentIdentity = ((data.length - hamming) / data.length * 100).toFixed(2);
    const errorMargin = (100 - percentIdentity).toFixed(2);
    return { hamming, errorRate, percentIdentity, errorMargin };
}

