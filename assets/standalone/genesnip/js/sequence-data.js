// Canvas Drawing Functions
/**
 * Draws the main chromatogram canvas.
 */
function drawCanvas() {
    const canvas = document.getElementById('chromatogram-holder');
    const ctx = canvas.getContext('2d');
    const w = canvas.offsetWidth;
    const h = canvas.offsetHeight;
    const dpr = window.devicePixelRatio || 1;
    canvas.width = w * dpr;
    canvas.height = h * dpr;
    ctx.scale(dpr, dpr);
    ctx.clearRect(0, 0, w, h);

    const laneWidth = (w - 15) / dnaData.length;

    // Draw nucleotide bands
    NUCLEOTIDE_BANDS.forEach(band => {
        const top_y = h - (band.max * h);
        const band_height = (band.max - band.min) * h;
        ctx.fillStyle = band.color;
        ctx.fillRect(0, top_y, w, band_height);
    });

    // Draw vertical grid lines
    ctx.strokeStyle = '#d3d3d3';
    ctx.lineWidth = 1;
    for (let i = 0; i < dnaData.length; i += 5) {
        const grid_x = i * laneWidth + 7.5;
        ctx.beginPath();
        ctx.moveTo(grid_x, 0);
        ctx.lineTo(grid_x, h);
        ctx.stroke();
    }

    // Draw data curves and lines
    dnaData.forEach((item, i) => {
        const x_start = i * laneWidth + 7.5;
        const pdata_val = parseFloat(item.pdata);
        const tdata_val = parseFloat(item.tdata);
        const error_amplitude = Math.abs(pdata_val - tdata_val) * h;

        // Error curve (red)
        ctx.beginPath();
        ctx.moveTo(x_start, h);
        for (let j = 0; j <= laneWidth; j += 1) {
            const t = (j / laneWidth) * Math.PI;
            ctx.lineTo(x_start + j, h - (Math.sin(t) * error_amplitude));
        }
        ctx.lineTo(x_start + laneWidth, h);
        ctx.strokeStyle = '#f00';
        ctx.stroke();

        // pdata line (orange)
        ctx.beginPath();
        ctx.moveTo(x_start, h - (pdata_val * h));
        ctx.lineTo(x_start + laneWidth, h - (pdata_val * h));
        ctx.strokeStyle = '#ffa500';
        ctx.stroke();

        // tdata line (blue)
        ctx.beginPath();
        ctx.moveTo(x_start, h - (tdata_val * h));
        ctx.lineTo(x_start + laneWidth, h - (tdata_val * h));
        ctx.strokeStyle = '#00f';
        ctx.stroke();
    });

    // Draw crosshair
    if (mouseX >= 0 && mouseY >= 0) {
        ctx.strokeStyle = 'rgb(90, 89, 92)';
        ctx.beginPath();
        ctx.moveTo(mouseX, 0);
        ctx.lineTo(mouseX, h);
        ctx.moveTo(0, mouseY);
        ctx.lineTo(w, mouseY);
        ctx.stroke();
    }
}

/**
 * Draws the selected chromatogram canvas.
 */
function drawSelectedCanvas() {
    const canvas = document.getElementById('selected-chromatogram');
    const ctx = canvas.getContext('2d');
    const w = canvas.offsetWidth;
    const h = canvas.offsetHeight;
    const dpr = window.devicePixelRatio || 1;
    canvas.width = w * dpr;
    canvas.height = h * dpr;
    ctx.scale(dpr, dpr);
    ctx.clearRect(0, 0, w, h);

    if (!selectedPosition) return;

    const item = dnaData.find(d => d.position === selectedPosition);
    if (!item) return;

    // Vertical scaling
    const dataMin = 0.1;
    const dataMax = 0.96;
    const canvasTop = h * 0.1;
    const canvasBottom = h * 0.9;
    const scaleY = (canvasBottom - canvasTop) / (dataMax - dataMin);

    // Draw grid
    ctx.strokeStyle = '#d3d3d3';
    ctx.lineWidth = 1;
    for (let y = 0; y <= h; y += h / 50) {
        ctx.beginPath();
        ctx.moveTo(0, y);
        ctx.lineTo(w, y);
        ctx.stroke();
    }

    const barWidth = w / 4;
    const gap = w / 8;
    const x_ref = w / 4;
    const x_query = x_ref + barWidth + gap;

    ctx.beginPath();
    ctx.moveTo(x_ref + barWidth / 2, 0);
    ctx.lineTo(x_ref + barWidth / 2, h);
    ctx.moveTo(x_query + barWidth / 2, 0);
    ctx.lineTo(x_query + barWidth / 2, h);
    ctx.stroke();

    // Draw y-axis
    ctx.font = '10px Arial';
    ctx.textAlign = 'right';
    ctx.fillStyle = '#000000';
    for (let value = 0.1; value <= 1.0; value += 0.1) {
        const y = canvasBottom - ((value - dataMin) * scaleY);
        ctx.beginPath();
        ctx.moveTo(0, y);
        ctx.lineTo(5, y);
        ctx.stroke();
        ctx.fillText(value.toFixed(1), 20, y + 3);
    }

    // Draw bars
    const fixedBarHeight = 16;
    const pdata_val = parseFloat(item.pdata);
    const tdata_val = parseFloat(item.tdata);

    ctx.fillStyle = '#C71585';
    ctx.fillRect(x_ref, canvasBottom - ((pdata_val - dataMin) * scaleY), barWidth, fixedBarHeight);

    ctx.fillStyle = '#4A8282';
    ctx.fillRect(x_query, canvasBottom - ((tdata_val - dataMin) * scaleY), barWidth, fixedBarHeight);

    // Draw labels
    ctx.textAlign = 'center';
    ctx.fillText('Reference', x_ref + barWidth / 2, canvasBottom - ((pdata_val - dataMin) * scaleY) - 10);
    ctx.fillText('Query', x_query + barWidth / 2, canvasBottom - ((tdata_val - dataMin) * scaleY) - 10);

    // Draw crosshair
    if (selectedMouseX >= 0 && selectedMouseY >= 0) {
        ctx.strokeStyle = 'rgb(90, 89, 92)';
        ctx.beginPath();
        ctx.moveTo(selectedMouseX, 0);
        ctx.lineTo(selectedMouseX, h);
        ctx.moveTo(0, selectedMouseY);
        ctx.lineTo(w, selectedMouseY);
        ctx.stroke();
    }
}

