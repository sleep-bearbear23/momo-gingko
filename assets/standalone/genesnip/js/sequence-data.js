/**
 * Updates the sequence diagnosis metrics display.
 * @param {Array} data - DNA data array.
 */
function sequenceDiagnosis(data) {
    const metrics = computeOverallMetrics(data);
    document.getElementById("hd").textContent = metrics.hamming;
    document.getElementById("er").textContent = `${metrics.errorRate}%`;
    document.getElementById("pi").textContent = `${metrics.percentIdentity}%`;
    document.getElementById("em").textContent = `${metrics.errorMargin}%`;
}

/**
 * Updates the diagnosis table based on selected item.
 * @param {Object|null} item - Selected DNA data item or null.
 */
function updateHoverTable(item) {
    const status = item ? (item.pn === item.tn && item.pl === item.tl ?
        '<span class="status-matched">MATCHED</span>' :
        '<span class="status-mismatched">⚠ MISMATCHED</span>') : "";
    const pairwiseError = item ? (Math.abs(parseFloat(item.pdata) - parseFloat(item.tdata)) * 100).toFixed(2) : "";

    document.getElementById("diag-status").innerHTML = status;
    document.getElementById("diag-pos").textContent = item ? `11p15.1-${item.position}` : "";
    document.getElementById("diag-r").textContent = item ? `${item.pdata} (${item.pl})` : "";
    document.getElementById("diag-q").textContent = item ? `${item.tdata} (${item.tl})` : "";
    document.getElementById("pe").textContent = pairwiseError ? `${pairwiseError}%` : "";
}

