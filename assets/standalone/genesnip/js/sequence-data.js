// Event Setup Functions
/**
 * Sets up click events for the main table rows.
 * @param {Array} data - DNA data array.
 */
function setupMainTableEvents(data) {
    document.querySelectorAll(".diagram tbody tr").forEach(row => {
        row.addEventListener("click", event => {
            const position = event.target.closest("td").getAttribute("data-position");
            if (!position) return;

            if (selectedPosition === position) {
                selectedPosition = null;
                document.querySelectorAll(".diagram tbody tr").forEach(r => r.classList.remove("selected"));
                updateEditTable(null);
                updateHoverTable(null);
                clearSelectedCanvasToWhite();
            } else {
                selectedPosition = position;
                document.querySelectorAll(".diagram tbody tr").forEach(r => r.classList.remove("selected"));
                row.classList.add("selected");
                const item = data.find(d => d.position === position);
                updateEditTable(item);
                updateHoverTable(item);
                drawSelectedCanvas();
            }

            document.getElementById("editPopup").style.display = "none";
            document.getElementById("popup-greyout").style.display = "flex";
        });
    });
}

